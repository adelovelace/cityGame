public class Location {

    public int row;
    public int column;

    //constructor
    Location(){}


    //getters

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }

    //setters

    public void setRow(int row) {
        this.row = row;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
